#Вывести на экран большее из этих чисел
a = float(input('введите число 1 '))
b = float(input('введите число 2 '))
if a > b:
  print(f'{int(a)} - наибольшее число')
elif a == b:
  print(f'наибольшее число {int(a)} - числа равны')
else:
  print(f'{int(b)} - наибольшее число')

