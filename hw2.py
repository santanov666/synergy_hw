#Задание 1
#Пользователь вводит целое число. Проверьте, является ли это число четным.
#Если число является четным, выведите «Данное число четное», если наоборот – выведите «Данное число не является четным»

num = int(input('введите число '))
if num % 2 == 0:
  print(f'{num} - четное число')
else:
  print(f'{num} - нечетное число')

#Задание 2
